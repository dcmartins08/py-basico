
#Contando elementos dentro da Tupla
#nome_tupla.count(valor)
tupla = (2,2,4,8)
print(f'o numero 2 aparece {tupla.count(4)} vezes')


#descobrir o índice de um elemento
#nome_tupla.index(valor)
tupla = (2,2,4,8)
print(f'o numero do índice {tupla.index(4)}')


#descobrir o valor máximo e mínimo
#max(tupla)   min(tupla)
tupla = (2,2,4,8)
print(f'o numero maior é {max(tupla)}')

tupla = (2,2,4,8)
print(f'o numero menor é {min(tupla)}')

